$('.lang-dropdown > ul > li').on('click',function(){

    var selectedLangauge = $(this).children('span').text();
    var langDisplaySpan = $('#selected-lang');

    if(selectedLangauge !== langDisplaySpan.text()){
        $(this).children('div').toggleClass('selected-lang');
        $(this).siblings().children('div').removeClass('selected-lang');

        langDisplaySpan.text($(this).children('span').text());
        var lang = $('#selected-lang').text();
        if(lang === 'English')
        {
           //google analytics english language selected event
            ga('send','event', 'Language Dropdown','Change Language','Selected English Language');

            $('.exer-detail-urdu').css("display","none");
            $('.exer-detail-engl').css("display","block");
        }
        else if( lang === 'Urdu')
        {
            //google analytics urdu language selected event
            ga('send','event', 'Language Dropdown','Change Language','Selected Urdu Language');
            $('.exer-detail-urdu').css("display","block");
            $('.exer-detail-engl').css("display","none");
        }
    }
});


function fill_cell_cont(){



    // get container width
    var cont_width = $('.cell_cont').width();
    // get single cell width
    var cell_width = $('.img_cls').width();

    var html = '';
    //get chapterNo - we will use this to get totalCells in .cell_cont
    var chapterNo = parseInt($('.select_lesson').find('.count_lesson-detail > div.counter').text()) - 1;

    // total no of cells in cell container
    // -3 is to skip first three object in lesson_data which are not part of cell container
    //var totalCells = lesson_data[chapterNo].length - 3;
    var totalCells = $('.img_cls.normal').toArray().length;

    // no of cells in each row
    var noOfCellsInARow = Math.floor(cont_width / cell_width);

    // get total no of cells
    var newtotalCells = totalCells;

    // while newTotalCells are equally divided by no of cells in a row
    while(newtotalCells % noOfCellsInARow != 0 ){
           newtotalCells++;
    }

    // get no of empty cells to append at the end of chapter
    var emptyCellsToAdd = newtotalCells - totalCells;
    for(var c = 0 ; c < emptyCellsToAdd; c++)
    {
        html += '<div class="img_cls empty"></div>'
    }

    //append empty cells
    $($('.img_cls.normal').last()).after(html);
}


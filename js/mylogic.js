function mark_as(clicked_switch, set_is_read)
{
    var selected_side_panel_box = $(clicked_switch).parents('.lesson_box');
    var is_sidepanel_switch = selected_side_panel_box.length == 0 ? false : true;
    var side_panel_lesson_num = selected_side_panel_box.attr('data-lesson-numb');
    var opened_lesson_num = Number($('.header').attr('data-lesson_numb'));
    var update_storage_for_lesson_no = 0;
    if(set_is_read == 0)
    {
        if(is_sidepanel_switch)
        {

            if(side_panel_lesson_num == opened_lesson_num)
            {
                clicked_switch.removeClass('checked');
                $('.switch-box .onoffswitch-checkbox').removeClass('checked');
            }
            else
            {
                clicked_switch.removeClass('checked');
            }
            update_storage_for_lesson_no = side_panel_lesson_num;
            ga('send','event', 'Mark ReadUnread','Mark Unread','Marked Chapter '+
                update_storage_for_lesson_no+' Unread from SidePanel',update_storage_for_lesson_no);
        }
        else
        {
             clicked_switch.removeClass('checked');
             $('.select_lesson .onoffswitch-checkbox').removeClass('checked');
             update_storage_for_lesson_no = opened_lesson_num;
             ga('send','event', 'Mark ReadUnread','Mark Unread','Marked Chapter '+
                update_storage_for_lesson_no+' Unread from Lesson', update_storage_for_lesson_no);
        }

        update_storage_read(update_storage_for_lesson_no, 0);
    }
    else
    {
        if(is_sidepanel_switch)
        {

            if(side_panel_lesson_num == opened_lesson_num)
            {
                clicked_switch.addClass('checked');
                $('.switch-box .onoffswitch-checkbox').addClass('checked');
            }
            else
            {
                clicked_switch.addClass('checked');
            }
                       update_storage_for_lesson_no = side_panel_lesson_num;
                       ga('send','event', 'Mark ReadUnread','Mark Read','Marked Chapter '+
                update_storage_for_lesson_no+' Read from Side panel', update_storage_for_lesson_no);
        }
        else
        {
             clicked_switch.addClass('checked');
             $('.select_lesson .onoffswitch-checkbox').addClass('checked');
                          update_storage_for_lesson_no = opened_lesson_num;
                          ga('send','event', 'Mark ReadUnread','Mark Read','Marked Chapter '+
                update_storage_for_lesson_no+' Read from Lesson', update_storage_for_lesson_no);
        }
        update_storage_read(update_storage_for_lesson_no, 1);

    }
}

